def slice_simple():
    """Dado el texto 'Awesome', imprime distintos substrings
    usando slicing y lower().
    """
    texto = "Awesome" #se establece una variable

    print(texto[0:3].lower())  #se imprime distintos substrings usando slicing y lower()
    print(texto[2:5])
    print(texto.lower())

slice_simple()
